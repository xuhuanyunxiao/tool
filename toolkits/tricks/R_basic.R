# R语言设置显示常用的命令是options 、format、signif等
# options主要是用来设置可以改变R的计算和显示结果全局选项。
# 如果用该命令后还是显示不全，则可以把变量转换成字符型然后再转换成数值型，再进行显示。
options(digits = 7)           # 控制要打印数值的位数（最大只能到22）
options(scipen = 100)     # 平时常用的数值或科学计数法输出，此处用于设置显示的位数

























